# Cree un programa que, al recibir como datos dos números enteros, calcule la suma , resta, y multiplicación de dichos números.

n1 = float(input("Ingrese el primer número: "))
n2 = float(input("Ingrese el segunfo número: "))

suma = n1+n2
resta = n1-n2
multi = n1*n2

print("La suma es ",suma)
print("La resta es ",resta)
print("La multiplicación es ",multi)